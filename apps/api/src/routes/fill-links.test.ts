describe('GET /fill/:token (public, no auth)', () => {
  it('serves the current published version fields + branding with no cookie', async () => {
    mockDbValue = fakeDb({
      fillLinksFindFirst: ACTIVE_LINK,
      formTemplatesFindFirst: PUBLISHED_TEMPLATE,
      formTemplateVersionsFindFirst: PUBLISHED_V1,
      organizationsFindFirst: { id: 'org-1', name: 'Charles Hull', plan: 'Business', branding: BRANDING },
    }).db;
    const { server, base } = startApp();
    try {
      const res = await fetch(`${base}/fill/${ACTIVE_LINK.token}`);
      expect(res.status).toBe(200);
      const body = (await res.json()) as Record<string, unknown>;
      expect(body).toEqual({
        formName: 'Vendor onboarding',
        orgName: 'Charles Hull',
        orgBranding: BRANDING,
        versionId: 'v1',
        fields: PUBLISHED_V1.fields,
        container: CONTAINER,
      });
      // Never leak org internals beyond name/branding.
      expect(body.plan).toBeUndefined();
      expect(body.orgId).toBeUndefined();
    } finally {
      server.close();
    }
  });

  /**
   * An org that never opened the theme editor must keep the exact payload it
   * had before theming existed — this is the AE3 guarantee on the wire, and
   * this route is the most-hit anonymous endpoint in the product.
   */
  it('omits the theme entirely when neither the org nor the form sets one', async () => {
    mockDbValue = fakeDb({
      fillLinksFindFirst: ACTIVE_LINK,
      formTemplatesFindFirst: PUBLISHED_TEMPLATE,
      formTemplateVersionsFindFirst: PUBLISHED_V1,
      organizationsFindFirst: { id: 'org-1', name: 'Charles Hull', branding: BRANDING },
    }).db;
    const { server, base } = startApp();
    try {
      const res = await fetch(`${base}/fill/${ACTIVE_LINK.token}`);
      const body = (await res.json()) as { orgBranding: Record<string, unknown> };
      expect(body.orgBranding.theme).toBeUndefined();
    } finally {
      server.close();
    }
  });

  it('resolves the org theme into the payload when the org sets one', async () => {
    mockDbValue = fakeDb({
      fillLinksFindFirst: ACTIVE_LINK,
      formTemplatesFindFirst: PUBLISHED_TEMPLATE,
      formTemplateVersionsFindFirst: PUBLISHED_V1,
      organizationsFindFirst: {
        id: 'org-1',
        name: 'Charles Hull',
        branding: { ...BRANDING, theme: { radius: 4, density: 'compact' } },
      },
    }).db;
    const { server, base } = startApp();
    try {
      const res = await fetch(`${base}/fill/${ACTIVE_LINK.token}`);
      const body = (await res.json()) as {
        orgBranding: { theme: Record<string, unknown> };
      };
      expect(body.orgBranding.theme.radius).toBe(4);
      expect(body.orgBranding.theme.density).toBe('compact');
      // Fully resolved: keys neither layer set still arrive with defaults, so
      // the public page never has to know the precedence rule.
      expect(body.orgBranding.theme.layout).toBe('card');
    } finally {
      server.close();
    }
  });

  /**
   * Covers AE4. The form override wins on the keys it sets and inherits the
   * org's value everywhere else — not the product default.
   */
  it('lets a form override win over the org theme, inheriting the rest', async () => {
    mockDbValue = fakeDb({
      fillLinksFindFirst: ACTIVE_LINK,
      formTemplatesFindFirst: {
        ...PUBLISHED_TEMPLATE,
        themeOverride: { radius: 20 },
      },
      formTemplateVersionsFindFirst: PUBLISHED_V1,
      organizationsFindFirst: {
        id: 'org-1',
        name: 'Charles Hull',
        branding: { ...BRANDING, theme: { radius: 4, density: 'compact' } },
      },
    }).db;
    const { server, base } = startApp();
    try {
      const res = await fetch(`${base}/fill/${ACTIVE_LINK.token}`);
      const body = (await res.json()) as {
        orgBranding: { theme: Record<string, unknown> };
      };
      expect(body.orgBranding.theme.radius).toBe(20); // form wins
      expect(body.orgBranding.theme.density).toBe('compact'); // inherited from org
    } finally {
      server.close();
    }
  });

  it('resolves a form override even when the org has no theme', async () => {
    mockDbValue = fakeDb({
      fillLinksFindFirst: ACTIVE_LINK,
      formTemplatesFindFirst: { ...PUBLISHED_TEMPLATE, themeOverride: { layout: 'hero' } },
      formTemplateVersionsFindFirst: PUBLISHED_V1,
      organizationsFindFirst: { id: 'org-1', name: 'Charles Hull', branding: BRANDING },
    }).db;
    const { server, base } = startApp();
    try {
      const res = await fetch(`${base}/fill/${ACTIVE_LINK.token}`);
      const body = (await res.json()) as { orgBranding: { theme: Record<string, unknown> } };
      expect(body.orgBranding.theme.layout).toBe('hero');
    } finally {
      server.close();
    }
  });

  it('404s identically for unknown, revoked, and expired tokens', async () => {
    const { db, query } = fakeDb({});
    query.fillLinks.findFirst
      .mockResolvedValueOnce(undefined) // unknown
      .mockResolvedValueOnce({ ...ACTIVE_LINK, active: false }) // revoked
      .mockResolvedValueOnce({ ...ACTIVE_LINK, expiresAt: new Date('2026-01-01T00:00:00Z') }); // expired
    mockDbValue = db;
    const { server, base } = startApp();
    try {
      const unknown = await fetch(`${base}/fill/nope`);
      const revoked = await fetch(`${base}/fill/${ACTIVE_LINK.token}`);
      const expired = await fetch(`${base}/fill/${ACTIVE_LINK.token}`);
      expect(unknown.status).toBe(404);
      expect(revoked.status).toBe(404);
      expect(expired.status).toBe(404);
      const bodies = await Promise.all([unknown.json(), revoked.json(), expired.json()]);
      expect(bodies[1]).toEqual(bodies[0]);
      expect(bodies[2]).toEqual(bodies[0]);
    } finally {
      server.close();
    }
  });

  it('serves the NEW version fields after a newer version is published (fix propagation)', async () => {
    const v2 = {
      id: 'v2',
      templateId: 't1',
      state: 'published',
      fields: [{ id: 'abn', type: 'text', label: 'ABN (11 digits)', required: true }],
      container: CONTAINER,
    };
    mockDbValue = fakeDb({
      fillLinksFindFirst: ACTIVE_LINK,
      formTemplatesFindFirst: { ...PUBLISHED_TEMPLATE, currentVersionId: 'v2' },
      formTemplateVersionsFindFirst: v2,
      organizationsFindFirst: { id: 'org-1', name: 'Charles Hull', branding: BRANDING },
    }).db;
    const { server, base } = startApp();
    try {
      const res = await fetch(`${base}/fill/${ACTIVE_LINK.token}`);
      expect(res.status).toBe(200);
      const body = (await res.json()) as { versionId: string; fields: unknown };
      expect(body.versionId).toBe('v2');
      expect(body.fields).toEqual(v2.fields);
    } finally {
      server.close();
    }
  });

  it('keeps serving an ARCHIVED template — archive is metadata-only, the public path reads version state', async () => {
    mockDbValue = fakeDb({
      fillLinksFindFirst: ACTIVE_LINK,
      formTemplatesFindFirst: { ...PUBLISHED_TEMPLATE, status: 'archived' },
      formTemplateVersionsFindFirst: PUBLISHED_V1,
      organizationsFindFirst: { id: 'org-1', name: 'Charles Hull', branding: BRANDING },
    }).db;
    const { server, base } = startApp();
    try {
      const res = await fetch(`${base}/fill/${ACTIVE_LINK.token}`);
      expect(res.status).toBe(200);
      const body = (await res.json()) as { versionId: string };
      expect(body.versionId).toBe('v1');
    } finally {
      server.close();
    }
  });
});

