function fakeDb(opts: {
  formTemplatesFindFirst?: unknown;
  formTemplateVersionsFindFirst?: unknown;
  fillLinksFindFirst?: unknown;
  fillLinksFindMany?: unknown[];
  organizationsFindFirst?: unknown;
  rolePermissionsFindFirst?: unknown;
  usersFindFirst?: unknown;
  /** Table whose insert `.values(...)` rejects — simulates a mid-write DB failure. */
  failInsertOf?: unknown;
}) {
  const insertValues = vi.fn();
  const updateSet = vi.fn();
  const rolledBack = vi.fn();

  /** Inserts echo the values passed over DB column defaults, plus a canned
   *  id/createdAt — so tokens generated by the route flow through to the
   *  response assertions, like a real `.returning()` would. */
  function insertResult(table: unknown, v: Record<string, unknown>) {
    const row = { id: 'row-new', active: true, createdAt: new Date('2026-07-02T00:00:00Z'), ...v };
    const awaitable = Promise.resolve(undefined) as Promise<undefined> & {
      returning: () => Promise<unknown[]>;
    };
    awaitable.returning = vi.fn().mockResolvedValue([row]);
    return awaitable;
  }

  /** A `.values(...)` result that rejects, for the `failInsertOf` table. */
  function insertReject(table: unknown, v: Record<string, unknown>) {
    void v;
    const err = new Error('insert_failed');
    const awaitable = Promise.reject(err) as Promise<undefined> & {
      returning: () => Promise<unknown[]>;
    };
    awaitable.catch(() => {}); // keep the base awaitable from surfacing as unhandled
    awaitable.returning = vi.fn().mockRejectedValue(err);
    return awaitable;
  }

  /** Builds an `insert` fn whose `.values()` records via `record` and either
   *  resolves or (for `failInsertOf`) rejects. */
  function makeInsert(record: (table: unknown, v: Record<string, unknown>) => void) {
    return vi.fn((table: unknown) => ({
      values: (v: Record<string, unknown>) => {
        record(table, v);
        return table === opts.failInsertOf ? insertReject(table, v) : insertResult(table, v);
      },
    }));
  }

  const query = {
    formTemplates: { findFirst: vi.fn().mockResolvedValue(opts.formTemplatesFindFirst) },
    formTemplateVersions: { findFirst: vi.fn().mockResolvedValue(opts.formTemplateVersionsFindFirst) },
    fillLinks: {
      findFirst: vi.fn().mockResolvedValue(opts.fillLinksFindFirst),
      findMany: vi.fn().mockResolvedValue(opts.fillLinksFindMany ?? []),
    },
    organizations: { findFirst: vi.fn().mockResolvedValue(opts.organizationsFindFirst) },
    rolePermissions: { findFirst: vi.fn().mockResolvedValue(opts.rolePermissionsFindFirst) },
    users: { findFirst: vi.fn().mockResolvedValue(opts.usersFindFirst) },
  };

  const db = {
    query,
    insert: makeInsert(insertValues),
    update: vi.fn((table: unknown) => ({
      set: (v: unknown) => {
        updateSet(table, v);
        return { where: vi.fn().mockResolvedValue(undefined) };
      },
    })),
    // Runs the callback against a `tx` surface whose inserts are BUFFERED and
    // only replayed into `insertValues` on success — a thrown callback discards
    // the buffer (rollback) and records `rolledBack`, mirroring drizzle.
    transaction: vi.fn(async (fn: (tx: unknown) => Promise<unknown>) => {
      const buffered: [unknown, Record<string, unknown>][] = [];
      const tx = { query, insert: makeInsert((table, v) => buffered.push([table, v])) };
      try {
        const result = await fn(tx);
        for (const [table, v] of buffered) insertValues(table, v);
        return result;
      } catch (err) {
        rolledBack();
        throw err;
      }
    }),
  } as unknown as Db;

  return { db, query, insertValues, updateSet, rolledBack };
}

